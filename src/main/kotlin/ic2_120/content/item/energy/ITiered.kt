package ic2_120.content.item.energy

import ic2_120.content.energy.EnergyTier

/**
 * 能量等级接口
 *
 * 实现此接口的类具有能量等级（tier）属性，用于区分不同层级的能量设备。
 * **线缆**与**发电机/电池**对 tier 的语义不同，见下文。
 *
 * ## 对于线缆（电压等级 1–5）
 *
 * 线缆的 tier 表示**电压等级**，决定可承载的传输速率（EU/t）和电网中的角色：
 * - **等级 1**: 锡线（32 EU/t）
 * - **等级 2**: 铜线（128 EU/t）
 * - **等级 3**: 金线（512 EU/t）
 * - **等级 4**: 高压导线/铁线（2048 EU/t）
 * - **等级 5**: 玻璃纤维导线（8192 EU/t）
 *
 * 绝缘线缆与同材料裸线等级相同（仅绝缘层不同，电压等级一致）。
 *
 * ## 对于发电机与电池（等级 1–4）
 *
 * 发电机/电池的 tier 表示**能量层级**，每级对应不同的容量和输出能力：
 * - **等级 1 (Tier 1)**: 基础级（如 Re-Battery，容量 10k EU）
 * - **等级 2 (Tier 2)**: 中级（如 Advanced RE-Battery，容量 100k EU）
 * - **等级 3 (Tier 3)**: 高级（如 Energy Crystal，容量 1M EU）
 * - **等级 4 (Tier 4)**: 顶级（如 Lapotron Crystal，容量 10M EU）
 *
 * **充电规则**：
 * - 机器/发电机只能给**能量等级小于或等于**自己等级的电池充电
 * - 低等级机器无法给高等级电池充电（能量不足/电压过高）
 * - 高等级机器可以给低等级电池充电（会自动降压）
 *
 * **示例**：
 * - 等级 1 发电机 → 只能给等级 1 电池充电
 * - 等级 2 发电机 → 可以给等级 1 和等级 2 电池充电
 * - 等级 4 发电机 → 可以给等级 1、2、3、4 电池充电
 */
interface ITiered {
    /**
     * 能量等级（线缆为 1–5 电压等级，发电机/电池为 1–4 能量层级）。
     * 等级越高，线缆可承载的 EU/t 越大，或发电机/电池的容量与输出能力越大。
     */
    val tier: Int

    /**
     * 该等级对应的标称传输速率（EU/t），与 [EnergyTier.euPerTickFromTier] 一致。
     */
    fun nominalEuPerTick(): Long = EnergyTier.euPerTickFromTier(tier)
}
