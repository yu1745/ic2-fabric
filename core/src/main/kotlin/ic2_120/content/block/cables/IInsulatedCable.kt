package ic2_120.content.block.cables

/**
 * 绝缘导线接口。
 *
 * 绝缘等级决定可安全承受的电网电压（输出等级）：
 * - 1 倍绝缘：可绝缘 ≤128 EU（等级 2）
 * - 2 倍绝缘：可绝缘 ≤512 EU（等级 3）
 * - 3 倍绝缘：可绝缘 ≤2048 EU（等级 4）
 * - 玻纤导线：最高绝缘，绝不漏电（等级 5）
 *
 * 当电网输出等级超过导线绝缘等级时，仍会漏电造成伤害。
 */
interface IInsulatedCable {
    /** 绝缘等级（2–5）。等级 ≥ 电网输出等级时不会漏电。 */
    val insulationLevel: Int
}
